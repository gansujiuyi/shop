package com.jiuyi.net.mesage.product;


public class ProductRspMsg {
	
	private ProductRsp productRsp;
	
	private String retCode;
	
	private String retShow;

	public ProductRsp getProductRsp() {
		return productRsp;
	}

	public void setProductRsp(ProductRsp productRsp) {
		this.productRsp = productRsp;
	}

	public String getRetCode() {
		return retCode;
	}

	public void setRetCode(String retCode) {
		this.retCode = retCode;
	}

	public String getRetShow() {
		return retShow;
	}

	public void setRetShow(String retShow) {
		this.retShow = retShow;
	}

	
}
